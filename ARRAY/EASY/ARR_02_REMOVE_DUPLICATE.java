package ARRAY.EASY;

public class ARR_02_REMOVE_DUPLICATE {

    public static void myApproach(int arr[]){
        int copy = arr[0];
        for(int i=0;i<arr.length;i++){
            if(arr[i] == copy ){
                System.out.println();
            }
        }

    }
    public static void main(String[] args) {
        
        int arr[] = {1,1,2,2,3,3,3};

    }
    
}
